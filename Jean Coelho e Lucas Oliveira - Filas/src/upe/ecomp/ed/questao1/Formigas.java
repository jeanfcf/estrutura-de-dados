package upe.ecomp.ed.questao1;

public class Formigas {
	private String nome;
	private int prioridade;
	
	public Formigas(String nome, int prioridade) {
		this.nome = nome;
		this.prioridade = prioridade;
	}
	
	public String getNome() {
		return nome;
	}
	
	public int getPrioridade() {
		return prioridade;
	}

	
}
